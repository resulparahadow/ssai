// v0.4.2.0: SHA256 of canonical DEFAULT_TRAINING constant (after JS template
// literal interpretation — escape sequences like \` are unescaped before
// hashing, which is what the browser sees at runtime).
// MUST be regenerated whenever DEFAULT_TRAINING is intentionally edited.
// Easiest path: open the app, go to Settings → Models tab → click "Show
// current brain SHA256" button — copy the displayed value into this constant.
// The button shows what the BROWSER computes, which is the only authoritative
// value (Python or terminal SHA256 tools won't match because they don't
// interpret JS template literal escapes).
const DEFAULT_TRAINING_SHA256='a1bcbcef27519268fd005622a9e124965a4b8e7732c24cc5303ccda0760ade0e';

async function computeBrainSha256(text){
  const buf=new TextEncoder().encode(text);
  const hash=await crypto.subtle.digest('SHA-256',buf);
  return Array.from(new Uint8Array(hash)).map(b=>b.toString(16).padStart(2,'0')).join('');
}

async function verifyBrainTamper(supabaseBrainText){
  const codeHash=await computeBrainSha256(DEFAULT_TRAINING);
  const codeMatch=(codeHash===DEFAULT_TRAINING_SHA256);
  let supabaseMatch=null,supabaseHash=null;
  if(supabaseBrainText){
    supabaseHash=await computeBrainSha256(supabaseBrainText);
    supabaseMatch=(supabaseHash===DEFAULT_TRAINING_SHA256);
  }
  if(!codeMatch){
    return {ok:false,codeMatch,supabaseMatch,codeHash,supabaseHash,
      reason:'DEFAULT_TRAINING constant in code does not match its declared SHA256 — code itself was tampered or constant was edited without bumping the hash.'};
  }
  if(supabaseBrainText && !supabaseMatch){
    return {ok:false,codeMatch,supabaseMatch,codeHash,supabaseHash,
      reason:'Supabase brain row does not match canonical SHA256 — possible tampering, or stale row from a prior version. Code constant is the source of truth.'};
  }
  return {ok:true,codeMatch,supabaseMatch,codeHash,supabaseHash};
}

